package output.states

enum class State(val outputState: OutputState) {
    FORWARD(OutputState.MOVING_FORWARD),
    FORWARD_FIRST_LINE_STARTED(OutputState.MOVING_FORWARD),
    FORWARD_FIRST_LINE_ENDED(OutputState.MOVING_FORWARD),
    FORWARD_SECOND_LINE_STARTED(OutputState.MOVING_FORWARD),
    FORWARD_SECOND_LINE_ENDED(OutputState.MOVING_FORWARD),
    BACKWARD(OutputState.MOVING_BACKWARD),
    CUT_TOWARDS_END(OutputState.CUTTING_TOWARDS_END),
    PAUSE_BEFORE_CUTS(OutputState.FULL_STOP),
    CUT_TOWARDS_START(OutputState.CUTTING_TOWARDS_START),
    STOP(OutputState.FULL_STOP),
}